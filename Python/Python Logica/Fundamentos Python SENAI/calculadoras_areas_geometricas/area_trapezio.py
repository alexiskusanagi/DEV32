# area do trapézio ((base maior + base menor) * altura) /2

print('=============== Cálculo da area do Trapézio =============== ')

base_maior = float(input('Digite valor da Base Maior: '))
base_menor = float(input('Digite valor da Base Menor: '))
altura = float(input('Digite valor da Altura: '))

area = ((base_maior + base_menor) * altura)/ 2

print(f'A área do Trapézio é: {area:.2f}') #a formatação .2f é para float e o .2 é 2 números da casa decimal ex: 2.45. existe .3f




